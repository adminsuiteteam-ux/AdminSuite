export function getMetrics() {
  const totalIncome = transactions
    .filter((t) => t.type === "income")
    .reduce((s, t) => s + t.amount, 0);
  const totalExpense = transactions
    .filter((t) => t.type === "expense")
    .reduce((s, t) => s + t.amount, 0);
  return {
    employees: employees.filter((e) => e.status !== "terminated").length,
    activeProjects: projects.filter((p) => p.status === "active").length,
    clients: clients.length,
    netProfit: totalIncome - totalExpense,
    totalIncome,
    totalExpense,
  };
}

export function getClientMetrics() {
  const active = clients.filter((c) => c.status === "active").length;
  const pending = clients.filter((c) => c.status === "pending").length;
  const completed = clients.filter((c) => c.status === "completed").length;
  return { active, pending, completed, total: clients.length };
}

export function getPayrollMetrics() {
  const paid = payrollMonths.filter((m) => m.paid).length;
  const unpaid = payrollMonths.length - paid;
  const staffPaid = employees.filter((e) => e.status === "active").length;
  return { paid, unpaid, staffPaid, total: payrollMonths.length };
}
